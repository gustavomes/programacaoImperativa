// Método Math.min(), retorna o número com o valor mais baixo.
let numeroMenor = Math.min(5, 10, 1, 2, 9, 55, 77, 99);
console.log("O menor valor é ........: ",numeroMenor);

// Método Math.max(), retorna o número com o valor mais alto.
let numeroMaior = Math.max(5, 10, 1, 2, 9, 55, 77, 99);
console.log("O maior valor é ........: ",numeroMaior);

let maiorNumArray = Math.max( [1, 2, 3] ); // Retorna: NaN
console.log("O maior valor no array é.....",maiorNumArray);